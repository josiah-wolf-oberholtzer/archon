for filename in os.scandir(destination_db):

  if (filename.path.endswith(".wav")):

    y, sr = librosa.load(filename, sr=sr)
    c_buff = []
    f_buff = []
    r_buff = []
    v_buff = []
    p_buff = []

    c_buff.append(
        np.ndarray.flatten(
        librosa.feature.spectral_centroid(y=y, sr=sr, hop_length=hop)))
    f_buff.append(
        np.ndarray.flatten(
        librosa.feature.spectral_flatness(y=y, hop_length=hop)))
    r_buff.append(
        np.ndarray.flatten(
        librosa.feature.spectral_rolloff(y=y, sr=sr, hop_length=hop)))
    v_buff.append(
        np.ndarray.flatten(
        librosa.feature.rms(y=y, hop_length=hop)))
    p_buff.append(
        np.ndarray.flatten(
        librosa.yin(y,80,10000, hop_length=hop)))
    
    id = filename.name
    cent = np.median(c_buff)
    flat = np.median(f_buff)
    rolloff = np.median(r_buff)
    rms = np.median(v_buff)
    pitch = str(librosa.hz_to_note(np.median(p_buff)))

    pitch = pitch.replace("â™¯", "#") 
    oct = int(pitch[-1])

    data.append({ 
            "id": id,
            "cent": str(cent),
            "flat": str(flat),
            "rolloff": str(rolloff),
            "rms": str(rms),
            "pitch": pitch
        })

    counter += 1
    if (counter % 1000 == 0): print("completed " + str(counter) + " of " + str(dest_datasize))

savefile = output_filename
with open(savefile, 'a') as outfile:
  json.dump(data, outfile, indent=2)